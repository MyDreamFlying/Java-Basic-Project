package MainPackage;

// 항공조회
public class FlightDTO extends DBConnection{

	String flight_no;
	String air_code;
	String apt_avl;
	String flight_depa;
	String flight_dept;
	String flight_dep;
	
	
	
	
	public String getFlight_no() {
		return flight_no;
	}
	public void setFlight_no(String flight_no) {
		this.flight_no = flight_no;
	}
	public String getAir_code() {
		return air_code;
	}
	public void setAir_code(String air_code) {
		this.air_code = air_code;
	}
	public String getApt_avl() {
		return apt_avl;
	}
	public void setApt_avl(String apt_avl) {
		this.apt_avl = apt_avl;
	}
	public String getFlight_depa() {
		return flight_depa;
	}
	public void setFlight_depa(String flight_depa) {
		this.flight_depa = flight_depa;
	}
	public String getFlight_dept() {
		return flight_dept;
	}
	public void setFlight_dept(String flight_dept) {
		this.flight_dept = flight_dept;
	}
	public String getFlight_dep() {
		return flight_dep;
	}
	public void setFlight_dep(String flight_dep) {
		this.flight_dep = flight_dep;
	}
	
	
	
	
}
